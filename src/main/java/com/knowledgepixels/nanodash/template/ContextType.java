/**
 * Enum representing different types of contexts in the application.
 */
public enum ContextType {

    /**
     * Represents an assertion context.
     */
    ASSERTION,

    /**
     * Represents a provenance context.
     */
    PROVENANCE,

    /**
     * Represents a publication information context.
     */
    PUBINFO
}
